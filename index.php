                         <p>CLIP is a theme-based program that helps English as a Second Language learners prepare for university study.  The emphasis of the program is on reading and writing for academic purposes.  The program is for ESL students who are accepted to the City University of New York (CUNY).  Students who attend the program are expected to attend City University after completing CLIP.  The program is voluntary for entering college freshmen who have failed the CUNY placement test in Reading and Writing.</p>

                        <p>Students engage in content-based instruction by studying topics related to immigration, civics, history, health, science, social science, etc. Classroom tutors are available. Students also participate in field trips to the theatre, museums, and NYC landmarks.</p>

                        <p>CLIP students can remain in the KCC CLIP program for a maximum of 900 hours:  Fall 300 hours; Winter 150 hours; Spring 300 hours; Summer 150 hours, but are free to return to college sooner.  Students can study in the day from 8:45 a.m. – 2:30 p.m. or in the evening from 5:00 p.m. – 10:00 p.m. Monday through Friday.</p>

                        Students must take the CUNY ACT Freshman Skills Assessment Test before or upon entering the program.  Eligible students are retested (only once) on the CUNY assessment tests in order to be placed in the appropriate classes when they return to college.
                     
